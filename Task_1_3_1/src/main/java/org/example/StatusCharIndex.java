package org.example;

/**
 * CharIndex class.
 */
public class StatusCharIndex {
    public int status;
    public char character;
    public int index;

    /**
     * Constructor.
     */
    public StatusCharIndex(int status, char character, int index) {
        this.status = status;
        this.character = character;
        this.index = index;
    }
}